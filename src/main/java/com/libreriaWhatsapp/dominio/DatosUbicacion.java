package com.libreriaWhatsapp.dominio;

public class DatosUbicacion {
	private String strNombre;
	private String strDireccion;
	private Double doubleLongitud;
	private Double doublLatitud;
	public String getStrNombre() {
		return strNombre;
	}
	public void setStrNombre(String strNombre) {
		this.strNombre = strNombre;
	}
	public String getStrDireccion() {
		return strDireccion;
	}
	public void setStrDireccion(String strDireccion) {
		this.strDireccion = strDireccion;
	}
	public Double getDoubleLongitud() {
		return doubleLongitud;
	}
	public void setDoubleLongitud(Double doubleLongitud) {
		this.doubleLongitud = doubleLongitud;
	}
	public Double getDoublLatitud() {
		return doublLatitud;
	}
	public void setDoublLatitud(Double doublLatitud) {
		this.doublLatitud = doublLatitud;
	}
	
	
}
